void draw_highlighted_square_wide_wide(uint16_t x, uint16_t y)
{
    uint32_t i, j, k = 0;
    for (j = y; j < y + 28; j++)
    {
        for (i = x; i < x + 124; i++)
        {
            if (i - x > 8 && i - x <= 104) // 96 pixel hole in the middle  to
                                           // stretch it
                LCD_SetPoint(i, j, TRANSPARENT);
            else
                LCD_SetPoint(i, j, highlighted_square_data[k++]);
        }
    }
}

